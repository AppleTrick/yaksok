"""
YOLO11 학습용 데이터셋 구성 스크립트
- 라벨링 완료된 데이터를 train/val로 분할
- YOLO 형식의 data.yaml 생성
"""

import os
import shutil
import random
from pathlib import Path

def prepare_yolo_dataset(
    images_dir='../data/processed',
    labels_dir='../data/labels',
    output_dir='../datasets/supplements',
    train_ratio=0.8,
    seed=42
):
    """
    라벨링된 이미지를 YOLO 학습용 구조로 변환
    
    예상 입력 구조:
    data/processed/
        bottle/
            image1.jpg
        box/
            image2.jpg
    data/labels/
        bottle/
            image1.txt
        box/
            image2.txt
    
    출력 구조:
    datasets/supplements/
        images/
            train/
            val/
        labels/
            train/
            val/
        data.yaml
    """
    random.seed(seed)
    
    # 출력 폴더 생성
    output_path = Path(output_dir)
    for split in ['train', 'val']:
        (output_path / 'images' / split).mkdir(parents=True, exist_ok=True)
        (output_path / 'labels' / split).mkdir(parents=True, exist_ok=True)
    
    # 클래스 매핑
    class_map = {'bottle': 0, 'box': 1}
    
    all_files = []
    
    # 이미지와 라벨 수집
    for category in ['bottle', 'box']:
        img_category_path = Path(images_dir) / category
        label_category_path = Path(labels_dir) / category
        
        if not img_category_path.exists():
            print(f"⚠️ 이미지 폴더 없음: {img_category_path}")
            continue
            
        if not label_category_path.exists():
            print(f"⚠️ 라벨 폴더 없음: {label_category_path}")
            print(f"   → 먼저 라벨링을 완료하세요!")
            continue
        
        for img_file in img_category_path.iterdir():
            if not img_file.suffix.lower() in ['.jpg', '.jpeg', '.png', '.webp']:
                continue
            
            # 대응하는 라벨 파일 찾기
            label_file = label_category_path / f"{img_file.stem}.txt"
            
            if label_file.exists():
                all_files.append({
                    'image': img_file,
                    'label': label_file,
                    'category': category
                })
            else:
                print(f"⚠️ 라벨 없음: {img_file.name}")
    
    if not all_files:
        print("\n❌ 라벨링된 파일이 없습니다!")
        print("먼저 라벨링을 완료하세요:")
        print("  1. labelImg 설치: pip install labelImg")
        print("  2. labelImg 실행 후 data/processed 폴더 열기")
        print("  3. 라벨 저장 위치: data/labels/bottle 또는 data/labels/box")
        return
    
    # 랜덤 셔플 및 분할
    random.shuffle(all_files)
    split_idx = int(len(all_files) * train_ratio)
    train_files = all_files[:split_idx]
    val_files = all_files[split_idx:]
    
    # 파일 복사
    def copy_files(file_list, split):
        for item in file_list:
            # 이미지 복사
            dst_img = output_path / 'images' / split / item['image'].name
            shutil.copy2(item['image'], dst_img)
            
            # 라벨 복사
            dst_label = output_path / 'labels' / split / item['label'].name
            shutil.copy2(item['label'], dst_label)
    
    copy_files(train_files, 'train')
    copy_files(val_files, 'val')
    
    # data.yaml 생성
    yaml_content = f"""# YOLO11 영양제 검출 데이터셋
# Auto-generated by prepare_dataset.py

path: {output_path.absolute()}
train: images/train
val: images/val

# 클래스 정의
names:
  0: bottle
  1: box

# 클래스 수
nc: 2
"""
    
    yaml_path = output_path / 'data.yaml'
    with open(yaml_path, 'w', encoding='utf-8') as f:
        f.write(yaml_content)
    
    # 결과 출력
    print("\n" + "="*50)
    print("     YOLO 데이터셋 준비 완료!")
    print("="*50)
    print(f"📁 출력 위치: {output_path.absolute()}")
    print(f"📊 Train: {len(train_files)}개")
    print(f"📊 Val: {len(val_files)}개")
    print(f"📄 설정 파일: {yaml_path}")
    print("="*50)
    
    # 카테고리별 분포
    print("\n📈 카테고리별 분포:")
    for category in ['bottle', 'box']:
        train_count = sum(1 for f in train_files if f['category'] == category)
        val_count = sum(1 for f in val_files if f['category'] == category)
        print(f"  {category}: train={train_count}, val={val_count}")
    
    return str(yaml_path)

if __name__ == "__main__":
    prepare_yolo_dataset()
